export const translations: Record<Locale, TranslationDict> = { pt, en };

export const createTranslator = (locale: Locale) => {
  return (key: string, vars?: Record<string, string | number>) => {
    const template = translations[locale][key] ?? translations.pt[key] ?? key;
    if (!vars) return template;
    return Object.entries(vars).reduce((acc, [name, value]) => acc.replace(`{{${name}}}`, String(value)), template);
  };
};
